package ImpConcepts;

public class Emp {

	String Ename;
	private int Eid;
	private double sal;
	private String getEname() {
		return Ename;
	}
	private void setEname(String ename) {
		Ename = ename;
	}
	private int getEid() {
		return Eid;
	}
	private void setEid(int eid) {
		Eid = eid;
	}
	private double getSal() {
		return sal;
	}
	private void setSal(double sal) {
		this.sal = sal;
	}
	

}
